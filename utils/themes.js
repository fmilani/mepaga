export function bgSelector(theme) {
  let selector
  switch (theme) {
    case 'pink':
      selector = 'bg-pink-700'
      break
    case 'green':
      selector = 'bg-green-700'
      break
    case 'blue':
      selector = 'bg-blue-700'
      break
    case 'indigo':
      selector = 'bg-indigo-700'
      break
    case 'purple':
      selector = 'bg-purple-700'
      break
    case 'yellow':
      selector = 'bg-yellow-700'
      break
  }
  return selector
}
